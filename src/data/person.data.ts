const createSequenceId = (index: number) => String(index).padStart(4, '0');

export const personsRows: PersonModel[] = Array.from({ length: 89 }, (_, index) => {
  const firstName = firstNames[index % firstNames.length];
  const lastName = lastNames[(index + 7) % lastNames.length];
  const street = streets[index % streets.length];
  const streetNumber = 100 + index;

  return {
    id: createSequenceId(index),
    fullName: `${firstName} ${lastName}`,
    address: `${streetNumber} ${street}`,
    phone: `555-${String(1000 + index).slice(-4)}`,
    email: `${firstName.toLowerCase()}.${lastName.toLowerCase()}${index + 1}@example.com`,
    wife: index % 2 === 0,
    childrenCount: (index % 4) + 1,
  };
});
